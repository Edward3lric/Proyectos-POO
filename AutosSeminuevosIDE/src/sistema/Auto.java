package sistema;
/**
 *
 * @author h4lof
 */
public class Auto {
    // Atributos
    private int claveAuto;
    private String marca;
    private String modelo;
    private double precio;
    private int numeroSerie;
    private String ubicacion;
    private String datalles;
    private double kilometraje;
    private int año;
    
    // Getters & Setters

    public int getClaveAuto() {
        return claveAuto;
    }

    public void setClaveAuto(int claveAuto) {
        this.claveAuto = claveAuto;
    }

    public String getMarca() {
        return marca;
    }

    public void setMarca(String marca) {
        this.marca = marca;
    }

    public String getModelo() {
        return modelo;
    }

    public void setModelo(String modelo) {
        this.modelo = modelo;
    }

    public double getPrecio() {
        return precio;
    }

    public void setPrecio(double precio) {
        this.precio = precio;
    }

    public int getNumeroSerie() {
        return numeroSerie;
    }

    public void setNumeroSerie(int numeroSerie) {
        this.numeroSerie = numeroSerie;
    }

    public String getUbicacion() {
        return ubicacion;
    }

    public void setUbicacion(String ubicacion) {
        this.ubicacion = ubicacion;
    }

    public String getDatalles() {
        return datalles;
    }

    public void setDatalles(String datalles) {
        this.datalles = datalles;
    }

    public double getKilometraje() {
        return kilometraje;
    }

    public void setKilometraje(double kilometraje) {
        this.kilometraje = kilometraje;
    }

    public int getAño() {
        return año;
    }

    public void setAño(int año) {
        this.año = año;
    }
    
}
